export const Technologys = () => {
  const { t } = useThemeLang()
  const [activeIndex, setActiveIndex] = useState(0)
  const intervalRef = useRef<ReturnType<typeof setInterval> | null>(null)

  const resetInterval = () => {
    if (intervalRef.current) clearInterval(intervalRef.current)
    intervalRef.current = setInterval(() => {
      setActiveIndex(prev => {
        const next = (prev + 1) % cardsData.length
        return next
      })
    }, 3500)
  }

  const changeActive = (next: number) => {
    if (next === activeIndex) return
    setActiveIndex(next)
    resetInterval()
  }

  useEffect(() => {
    resetInterval()
    const handleVisibility = () => {
      if (document.hidden) {
        if (intervalRef.current) clearInterval(intervalRef.current)
      } else {
        resetInterval()
      }
    }
    document.addEventListener('visibilitychange', handleVisibility)
    return () => {
      if (intervalRef.current) clearInterval(intervalRef.current)
      document.removeEventListener('visibilitychange', handleVisibility)
    }
  }, [])

  return (
    <div className="technologys">
      <div className="section-heading">
        <span className="section-eyebrow scroll-animate">
          <Cpu size={18} />
          {t("eyebrow.tech")}
        </span>
        <h1 className="scroll-animate ">{t("tech.title")}</h1>
      </div>
      <div className="technologys__grid scroll-animate">
        {cardsData.map((card, i) => {
          const iconColor = card.iconColor
          const cardStyle = {
            '--accent': iconColor,
            '--icon-bg': card.iconBg,
          } as CSSProperties

          return (
            <div
              key={i}
              className={`tech-card ${activeIndex === i ? 'active' : ''}`}
              onClick={() => changeActive(i)}
              style={cardStyle}
            >
              <div className="tech-card__inner">
                <div
                  className="tech-card__icon"
                  style={{ color: iconColor }}
                >
  {card.icon}
</div>
<h3>{card.title}</h3>
                <p className="tech-card__description">
                  {card.description.map((part, index) => (
                    <span key={index} className={part.highlight ? 'tech-card__highlight' : undefined}>
                      {part.text}
                    </span>
                  ))}
                </p>
              </div>
            </div>
          )
        })}
      </div>
    </div>
  )
}
